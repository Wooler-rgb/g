export const GAME_END_YEAR  = 2026;
export const GAME_END_MONTH = 4;

export function nextMonth(year: number, month: number): { year: number; month: number } {
  return month === 12 ? { year: year + 1, month: 1 } : { year, month: month + 1 };
}

/**
 * Расширяет `crisis.years` до апреля 2026, добавляя пропущенные месяцы.
 * Идемпотентна: если `crisis` уже содержит апрель 2026, возвращает его без изменений.
 */
export function buildCrisisTimeline(crisis: Crisis): Crisis {
  const last = crisis.years[crisis.years.length - 1];
  let { year, month } = nextMonth(last.year, last.month);
  const endOrd = GAME_END_YEAR * 12 + GAME_END_MONTH;
  const extra: GameStep[] = [];

  while (year * 12 + month <= endOrd) {
    const event = EXTRA_EVENTS[`${year}-${month}`];
    extra.push({
      year,
      month,
      label: `${MONTHS_RU[month]} ${year}`,
      news: EXTRA_NEWS[`${year}-${month}`] ?? [],
      eventName: event?.eventName,
      eventDescription: event?.eventDescription,
      eventColor: event?.eventColor,
    });
    ({ year, month } = nextMonth(year, month));
  }

  return { ...crisis, years: [...crisis.years, ...extra] };
}
