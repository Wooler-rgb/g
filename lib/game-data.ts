export function getBotNetWorth(botIndex: number, _steps: unknown[], currentStepIndex: number): number {
  const rng = seeded(botIndex * 1337 + 42);
  let value = 1_000_000;
  for (let i = 1; i <= currentStepIndex; i++) {
    value *= (1 + (rng() * 0.5 - 0.15)); // -15% to +35% per step
  }
  return Math.max(100_000, value);
}

