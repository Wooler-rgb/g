export function formatPrice(price: number): string {
  if (price === 0) return 'Н/Д';
  if (price >= 1000) return price.toLocaleString('ru-RU', { maximumFractionDigits: 0 }) + ' ₽';
  if (price >= 1)    return price.toFixed(2) + ' ₽';
  return price.toFixed(4) + ' ₽';
}

export function formatMoney(amount: number): string {
  if (Math.abs(amount) >= 1_000_000) return (amount / 1_000_000).toFixed(2) + ' млн ₽';
  if (Math.abs(amount) >= 1_000)     return (amount / 1_000).toFixed(1) + ' тыс ₽';
  return amount.toFixed(0) + ' ₽';
}


