// ВСПОМОГАТЕЛЬНЫЙ ГЕНЕРАТОР (используется только в getBotNetWorth)
// ============================================================
function seeded(seed: number) {
  let s = seed | 1;
  return () => {
    s = Math.imul(48271, s) | 0;
    return ((s >>> 0) & 0x7fffffff) / 0x7fffffff;
  };
}

