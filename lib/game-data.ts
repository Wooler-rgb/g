/**
 * Кумулятивный коэффициент инфляции от startYear до endYear/endMonth.
 * Показывает во сколько раз обесценились деньги:
 * 1 000 000 ₽ в 2014 = ~1 800 000 ₽ номинально к 2018.
 */
export function getInflationMultiplier(
  startYear: number,
  endYear: number,
  endMonth: number,
): number {
  if (endYear < startYear) return 1;
  let m = 1;
  for (let y = startYear; y <= endYear; y++) {
    const annual = INFLATION_RATES[y] ?? 1.06;
    if (y === endYear) {
      m *= Math.pow(annual, endMonth / 12);
    } else {
      m *= annual;
    }
  }
  return m;
}

/** Номинальная сумма сейчас, эквивалентная amount в startYear по покупательной способности */
export function inflationAdjusted(amount: number, startYear: number, endYear: number, endMonth: number): number {
  return amount * getInflationMultiplier(startYear, endYear, endMonth);
}

