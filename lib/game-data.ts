/** Ключевая ставка ЦБ на заданный месяц (годовая, коэффициент) */
export function getKeyRate(year: number, month: number): number {
  const key = `${year}-${month}`;
  if (KEY_RATES[key] !== undefined) return KEY_RATES[key];
  // nearest earlier key
  for (let m = month - 1; m >= 1; m--) {
    const k = `${year}-${m}`;
    if (KEY_RATES[k] !== undefined) return KEY_RATES[k];
  }
  for (let y = year - 1; y >= 2014; y--) {
    for (let m = 12; m >= 1; m--) {
      const k = `${y}-${m}`;
      if (KEY_RATES[k] !== undefined) return KEY_RATES[k];
    }
  }
  return 0.08;
}
